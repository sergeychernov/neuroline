  if echo "$out" | grep -Eqi \
    'No entries found|NOT FOUND|No match|Status:\s*FREE|% No entries found|domain:\s*AVAILABLE'; then
    yn="Y"
  else
    yn="N"
  fi

  if [[ "$yn" == "Y" ]]; then
    printf "%-24s %s\n" "$domain" "$yn"
  fi
done
